  def main(args: Array[String]): Unit = {
    //Tipo de dato String.
    var tipo_string:String="Guillermo"
    println("string: "+tipo_string)
    //Tipo de dato integer.
    var tipo_num=23
    println("integer: "+tipo_num)
    //Tipo de dato doble.
    var tipo_doble=45.5679d
    println("double: "+tipo_doble)
    //Tipo de dato float
    var tipo_flotante=23.4f
    println("integer: "+tipo_num)
    ("float: "+tipo_flotante)
    //Tipo de dato byte
    var tipo_Byte = -67
    println("byte: "+tipo_Byte)
    //Tipo de dato boolean
    var tipo_buleano=true 
    println("Boolean: "+tipo_buleano)
    //Tipo de dato Short
    var tipo_short:Short=256
    println("short: "+tipo_short)
    //Tipo de dato Long
    var tipo_long:Long=1345
    println("long: "+tipo_long)
    //Tipo de dato Char
    var tipo_char='G'
    println("char: "+tipo_char)
  }
}
